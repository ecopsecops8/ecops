<h2>Frequently Asked Questions(FAQs)</h2>
<i class='far fa-lightbulb'></i><span>Are there any particular opening and closing hours for police stations?</span>
<div id="f1"> The basic functional unit of police is police station, which is open for 24 hours a day for hearing your voice.</div> <br>
    
    
    <i class='far fa-lightbulb'></i><span>If I am an affected person or if I see a crime is happening or any violation, what am I supposed to do?</span>
<div id="f2"> You can report at the police station in whose jurisdiction the event has taken place either by phone or visiting personally. You can even send your written complaint by post. If you have complaint against a police officer, you can send or report the complaint to the SHO & D.S.P. and even to the higher authorities.</div> <br>
 
    <i class='far fa-lightbulb'></i><span> What is an FIR and why sometimes FIR registration is denied?</span>
    <div id="f3"> FIR stands for First Information Report. Whenever anybody reports about any crime which discloses cognizable offence then information is entered into a register whose Performa is as Performa no - 24.5 ( I ) of Punjab Police Rule. It is the right of complainant to get an FIR registered if his complaint discloses any cognizable offence. It is the right of the complainant to get one copy of FIR free of cost, immediately from concerned police station. FIR can be registered either on written statement or verbal statement of complainant which is later reduced in writing by Police Officer and is signed by the complainant.

If your complaint does not disclose any offence or your complaint discloses only non-cognizable offence, then FIR cannot be registered. In such cases where only non-cognizable offence is made out, Police officer will enter the substance of information in the general diary of Police Station and give copy of same to complainant. Another copy of complaint is sent to the concerned judicial magistrate for further action. Police does not have powers to investigate a non-cognizable offence without the orders of the magistrate.</div> 
  <br>
    <i class='far fa-lightbulb'></i><span> What are cognizable and non-cognizable offences?</span>
<div id="f4"> Cognizable offence :- An offence for which a police officer has the powers to arrest without a warrant is defined as a cognizable offence. Offences like murder, rape ,kidnapping , theft , robbery , fraud etc. are classified as cognizable. To get detailed list of cognizable offence kindly refer to first schedule of Criminal Procedure Code. Non cognizable offence :- An offence for which a police officer does not have the power to arrest the accused without warrant. To get detailed list of non-cognizable offences please refer to first schedule of Criminal Procedure Code.</div> <br>
 
    <i class='far fa-lightbulb'></i><span> What is Bailable Offence and what is Non-Bailable Offence?</span>
<div id="f5"> Bailable Offences are those offences where the accused has the right to get himself released on bail after his arrest if he can give sureties or personal bond to the satisfaction of the police officer. For the list of bailable offences please refer to first schedule of Criminal Procedure Code.

Non-bailable offences are such offences where the police does not have the power to release the arrested person on bail (except under certain special circumstances ) . The arrested person is to be produced before the judicial magistrate within 24 hours of the arrest .For the list of non-bailable offences please refer to first schedule of Criminal Procedure Code.</div> <br>
 
    <i class='far fa-lightbulb'></i><span> Whether a police officer can arrest a person without warrant ?</span>
<div id="f6"> Yes, a police officer can arrest a person without warrant if he has been involved in any cognizable offence.</div> <br>
 
    <i class='far fa-lightbulb'></i><span>Is it possible to have police security for the individual property</span>
<div id="f7">Police department is meant for the security and safety of people and normally police patrolling is done by the police in the respective areas of their police stations. In the normal circumstances, it is not possible for police to look after the individual properties.</div> <br>
 
    <i class='far fa-lightbulb'></i><span> Whether a police officer can search a person / place without a search warrant ?</span>
<div id="f8"> Yes, a police officer can search for the person / place without search warrant under certain conditions.</div> <br>
 
    <i class='far fa-lightbulb'></i><span> Can I call the police on the basis of mere suspicion and if so, then on which number?</span>
<div id="f9"> In case you see anything unusual/suspicious, you can call the police anytime at 100 number which is toll free. Police will verify the facts and act accordingly.</div> <br>
 
    <i class='far fa-lightbulb'></i><span> How to call the police service for handling anonymous/threatening calls?</span>
<div id="f10"> If you get anonymous calls which may be threatening or harassing, you can inform the local police station or control room (100). It is advisable that you should also inform the telephone department to put your telephone under observation.</div> <br>
 
    <i class='far fa-lightbulb'></i><span> If I come to witness an accident, how should I react? Will there be any legal complications from the police department?</span>
<div id="f11">In such a situation, inform the nearest police station or dial 100.If there is no injury to anyone then wait for the police to come to the scene of the accident but if somebody is injured in the accident, it is your duty to take him to the nearest medical facility center, doctor on the duty is bound to give first aid to the injured without waiting for the police formalities. It is not necessary to take the injured to government hospital or to wait for the police to come.</div> <br>
 
    <i class='far fa-lightbulb'></i><span> What is the procedure for the complaints regarding the missing person?</span>
<div id="f12"> Report the matter to the police personally or on phone at 100 when you are sure that the person is missing.</div> <br>
 
    <i class='far fa-lightbulb'></i><span> What is the procedure regarding the reports and the F.I.Rs?</span>
<div id="f13"> You can report to the police when any crime occurs or when you apprehend a crime. Report could be on the telephone or in the writing. F.I.R. is lodged only when the crime has taken place.</div> <br>
 
    <i class='far fa-lightbulb'></i><span> How to inform regarding the wanted/missing person?</span>
<div id="f14">If you see a wanted criminal or a missing person, inform the local police or dial 100.</div> <br>
 
    <i class='far fa-lightbulb'></i><span> Why is police late in reacting?</span>
<div id="f15"> Police reacts on the information which it receives. Apparently, it appears that the police reach late on the site of accident or crime but actually, police starts its action when it gets the information and it has been noticed that the people are late in informing. So the promptness of the reaction depends on the promptness of the communication. The availability (shortage) of the resources also plays a major role.</div> <br>
    <i class='far fa-lightbulb'></i><span> Why is police rude and unreliable for the public?</span>
<div id="f16"> It is entirely a pre-conceived notion. The public is prejudiced in its view towards the police but the reality is that the expectations are very high and the force and facilities are very less.</div> <br>
 
    <i class='far fa-lightbulb'></i><span> How can I be a part of the police force?</span><br><br><br><br><br><br>
    <div id="f16"> If you wish to join the police department, you can enter at four stages if you are higher secondary pass, you can apply for the post of constable. If you are a graduate, you can apply for the post of sub inspector/platoon commander. The advertisements are normally given in the local dailies. If you are a graduate, you can appear for state PSC exam for Dy. S.P. If you are a graduate, you can also appear for UPSC exam to enter into IPS.</div> <br>

